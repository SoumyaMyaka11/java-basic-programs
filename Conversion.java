class Conversion
{
  public static void main(String args[])

  {
     
    char ch1 = 'a';
    int value1 = ch1;
    System.out.println("ASCII value of a is = " + value1);
    
    char ch2 = 'A';
    int value2 = ch2;
    System.out.println("ASCII value of A is = " + value2);
    
    int sum;
    sum = value1 + value2;
    System.out.println("sum of ASCII(a) and ASCII(A) is = " + sum);

    char ch3 = 'z';
    int value3 = ch3;
    System.out.println("ASCII value of z is = " + value3);

    char ch4 = 'Z';
    int value4 = ch4;
    System.out.println("ASCII value of Z is = " + value4);

    int sum1;
    sum1 = value3 + value4;
    System.out.println("sum of ASCII(z) and ASCII(Z) is = " + sum1);



  
   }

}